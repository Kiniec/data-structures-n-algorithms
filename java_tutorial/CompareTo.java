package java_tutorial;

public class CompareTo {
    public static void main (String[]args){
        String s1 = "Apple";
        String s2 = "apple";
        System.out.println(s1.compareTo(s2));
        System.out.println(s1.equals(s2));
        System.out.println(s1.equalsIgnoreCase(s2));
    }//end of main method

} //end of class
